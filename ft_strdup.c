#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*dup;
	size_t	i;

	i = ft_strlen(src) + 1;
	dup = (char *) malloc(i * sizeof(char));
	if (!dup)
		return (NULL);
	i = 0;
	while (src[i])
	{
		dup[i] = src[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
